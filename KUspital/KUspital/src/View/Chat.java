    public void saveChat(int uno)
    {
        save = new SaveChat();
        
        if(ms.my_type == 0)
        {
            save.Save(ms.DBoutput.get_information(uno, "ID") + ms.my_ID + ".txt", jTextArea1.getText());
        }
        else
        {
            save.Save(ms.my_ID + ms.DBoutput.get_information(uno, "ID") + ".txt", jTextArea1.getText());
        }
    }
    
